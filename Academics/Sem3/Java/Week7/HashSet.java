// Demonstrate HashSet.
import java.util.*;
class HashSe {
public static void main(String args[]) {
// Create a hash set.
HashSet <Integer> hs = new HashSet<Integer>();
// Add elements to the hash set.
hs.add("B");
hs.add("A");
hs.add("D");
hs.add("E");
hs.add("C");
hs.add("F");
System.out.println(hs);
}
}
